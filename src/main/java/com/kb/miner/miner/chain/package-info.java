/**
 * Contains general functionality to build the search lattice.
 * <p>
 * Because of its size, each node of a search lattice is build on demand. Therefore 
 * just the necessary parts are generated, hold in memory and traversed. Each 
 * {@link com.kb.miner.algorithms.Algorithm} has to provide its initial set of
 * {@link com.kb.miner.miner.chain.SearchLatticeNode} objects as root-nodes for the
 * search-lattice. The children of each node can be generated by the corresponding 
 * {@link com.kb.miner.miner.chain.Extender}. An extender generate just the necessary
 * (i.e. frequent) children, otherones will be ommited to prune the search lattice.<br/>
 * <img src="doc-files/SLN-Ext.png">
 * <p>
 * An {@link com.kb.miner.miner.chain.Extender} is organized as a pipeline (see picture below). The
 * {@link com.kb.miner.miner.chain.SearchLatticeNode} to be extended (1) is given to a chain of
 * {@link com.kb.miner.miner.chain.MiningStep} objects (2). Each {@link com.kb.miner.miner.chain.MiningStep}
 * offer another test, if or how the current node needs to be extended or stored. A required step 
 * is the {@link com.kb.miner.miner.chain.GenerationStep} (3), that generate all possible/allowed/necessary
 * {@link com.kb.miner.miner.chain.Extension}s of the current node. The generation is also be organized as
 * a two-way pipeline consisting of {@link com.kb.miner.miner.chain.GenerationPartialStep} objects.
 * The first part of the pipeline (A) is executed for each {@link com.kb.miner.miner.general.Embedding}
 * to determine extensions candidates. Afterwards, the second part (B) collects all requiered extensions
 * and give it to the mining chain (4). Those extensions can be applied to the parent node to 
 * generate each child (5).<br/>
 * <img src="doc-files/chain.png">
 * <p>
 * @author Marc Woerlein (woerlein@informatik.uni-erlangen.de)
 */
package com.kb.miner.miner.chain;
